#!/usr/bin/env node
// Render score trends across runs from the history ledger.
//
//   node scripts/eval-trend.js        (npm run eval:trend)
//   node scripts/eval-trend.js 20     (show the last 20 runs; default 15)
//
// Reads reports/eval/history.jsonl — one line per `npm run eval`, appended by
// scripts/eval.js — and prints a per-model "neutral Opus judge score over time"
// table, then writes the same as committable markdown to reports/eval/TREND.md.
// This is the one view neither the terminal heatmap nor `eval:view` shows well:
// is an asset/model drifting down over the last N runs?

import fs from "node:fs";
import path from "node:path";

const REPORT_DIR = path.resolve("reports/eval");
const HISTORY = path.join(REPORT_DIR, "history.jsonl");
const TREND_MD = path.join(REPORT_DIR, "TREND.md");
const LIMIT = Number(process.argv[2]) || 15;

if (!fs.existsSync(HISTORY)) {
  console.error(`No history ledger at ${path.relative(process.cwd(), HISTORY)} — run \`npm run eval\` first.`);
  process.exit(1);
}

const runs = fs
  .readFileSync(HISTORY, "utf8")
  .split("\n")
  .filter(Boolean)
  .map((l) => {
    try {
      return JSON.parse(l);
    } catch {
      return null;
    }
  })
  .filter(Boolean)
  .slice(-LIMIT);

if (!runs.length) {
  console.error("History ledger is empty — run `npm run eval` first.");
  process.exit(1);
}

// Every model seen across the shown runs becomes a column.
const models = [...new Set(runs.flatMap((r) => Object.keys(r.models || {})))].sort();
const score = (run, m) => {
  const v = run.models?.[m]?.primary;
  return typeof v === "number" ? v.toFixed(2) : "–";
};
const shortTs = (ts) => (ts || "").slice(0, 16).replace("T", " "); // YYYY-MM-DD HH:MM

// --- terminal table ---
const pad = (s, n) => String(s).padEnd(n);
const TSW = 18;
const SHAW = 9;
const COLW = 10;
console.log(`\nSCORE TREND — neutral Opus judge, foundation prompt (last ${runs.length} run(s))`);
console.log(pad("run", TSW) + pad("commit", SHAW) + models.map((m) => pad(m, COLW)).join(""));
for (const run of runs) {
  console.log(pad(shortTs(run.ts), TSW) + pad(run.commit || "?", SHAW) + models.map((m) => pad(score(run, m), COLW)).join(""));
}
console.log("\nHigher is better (0–1). Compare a model down its own column for drift; pass% per run is in reports/eval/history.jsonl.");

// --- committable markdown ---
const md = [];
md.push("<!-- generated by scripts/eval-trend.js — do not edit by hand; re-run `npm run eval:trend` -->");
md.push("# Eval score trend\n");
md.push(`_Neutral Opus judge · foundation prompt · last ${runs.length} run(s)_\n`);
md.push(`| run | commit | ${models.join(" | ")} |`);
md.push(`|---|---|${models.map(() => "---").join("|")}|`);
for (const run of runs) {
  md.push(`| ${shortTs(run.ts)} | \`${run.commit || "?"}\` | ${models.map((m) => score(run, m)).join(" | ")} |`);
}
md.push("\n_Quality is 0–1; higher is better. Read a model down its own column to spot drift. Per-run pass% lives in `reports/eval/history.jsonl`; full per-cell detail in `npm run eval:view`._");

fs.mkdirSync(REPORT_DIR, { recursive: true });
fs.writeFileSync(TREND_MD, md.join("\n") + "\n");
console.log(`\nWrote ${path.relative(process.cwd(), TREND_MD)} (committable trend).`);
